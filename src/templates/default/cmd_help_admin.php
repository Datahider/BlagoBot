<b>Команды администратора:</b>

<b>/update</b> - обновление основной базы данных бота из файла .xlsx
(для обновления второго файла просто отправьте файл боту);

<b>/users</b> - вывод списка пользователей в файле .pdf;
<b>/xusers</b> - вывод списка пользователей в файле .xlsx;

<b>/help</b> - вывод этого сообщения;
<b>/stat</b> - вывод отчета по статистике использования бота;
