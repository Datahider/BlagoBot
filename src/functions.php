function checkAdmin(?int $message_id=null) {
    global $b_user;
    
    if ($b_user->access_level === user::AL_ADMIN) {
        return true;
    } else {
        showAdminsOnly($message_id);
        return false;
    }
}

function sendSplitMessage($chat_id, $text, $separator='<!-- SPLIT -->') : void {
    
    $messages = explode($separator, $text);
    
    foreach ($messages as $message) {
        
        while (true) {
            if ($message) {
                Bot::$api->sendMessage($chat_id, $message, 'HTML');
            }
            break;
        }
    }
}

